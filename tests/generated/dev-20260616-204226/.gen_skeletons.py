#!/usr/bin/env python3
"""One-shot generator for test-writer subagent (task dev-20260616-204226).

Reads the BA acceptance-criteria JSON, emits one pytest skeleton per active AC
using the verbatim test-writer template (pytest.fail TEST_INCOMPLETE sentinel),
writes the per-task manifest (active_tests[] with verbatim hook_check), upserts
the task entry into the global index, and writes the report JSON.

This is a CREATE-only run (per-task dir + manifest absent, task not in index).
"""
import json
import os
import re
import sys
from datetime import datetime, timezone

REPO = "/dev/shm/dev-workspace/dot-claude"
TASK_ID = "dev-20260616-204226"
AC_PATH = os.path.join(REPO, "docs/dev/acceptance-criteria-%s.json" % TASK_ID)
TASK_DIR = os.path.join(REPO, "tests/generated", TASK_ID)
PER_TASK_MANIFEST = os.path.join(TASK_DIR, "manifest.json")
GLOBAL_INDEX = os.path.join(REPO, "tests/generated/manifest.json")
REPORT_PATH = os.path.join(REPO, "docs/dev/test-writer-report-%s.json" % TASK_ID)

CANONICAL_TYPES = {"ui", "api", "data", "hook"}
# Canonical required keys per type (test-writer.md spec). The BA AC file for this
# harness uses a shell-probe check shape {method, probe, expect_exit}; we treat a
# well-formed shell probe as valid for hook/data and record the variance.
REQUIRED_KEYS = {
    "ui": {"url", "viewports", "selectors", "assertions"},
    "api": {"endpoint", "method", "request", "assertions"},
    "data": {"file", "assertions"},
    "hook": {"script", "args", "assertions"},
}


def now_iso():
    return datetime.now(timezone.utc).strftime("%Y-%m-%dT%H:%M:%SZ")


def normalize_id(ac_id):
    return re.sub(r"[^0-9A-Za-z_]", "_", ac_id)


def one_line_summary(given, when, then):
    g = " ".join(str(given).split())
    w = " ".join(str(when).split())
    t = " ".join(str(then).split())

    def cap(s, n):
        return s if len(s) <= n else s[: n - 1].rstrip() + "…"

    return "GIVEN %s / WHEN %s / THEN %s" % (cap(g, 90), cap(w, 90), cap(t, 110))


def docstring_block(text, label):
    # Indent continuation lines to align under the GIVEN/WHEN/THEN label column.
    collapsed = " ".join(str(text).split())
    return "%s %s" % (label, collapsed)


SKELETON_TMPL = '''# Auto-generated by agents/test-writer.md from docs/dev/acceptance-criteria-{task_id}.json
# AC ID: {ac_id}  ac_uid: {ac_uid}  type: {ac_type}
# Dev is free to REPLACE the body of {func_name}() with a real
# implementation that asserts the GIVEN/WHEN/THEN behaviour. The metadata
# above (AC_UID, AC_TYPE, docstring) MUST be preserved verbatim so QA can
# trace each test back to its source AC entry.
#
# This is an infrastructure/hooks/scripts harness (no web app). This AC is a
# shell/exit-code probe (check.method == "shell"). Dev SHOULD implement the
# real body by shelling out to the probe described in HOOK_CHECK["probe"] from
# a fresh non-root clone and asserting the exit code matches
# HOOK_CHECK["expect_exit"] (and any stderr block-marker the AC names), per the
# test-writer agent spec for smoke/shell-verified ACs. Remove the
# TEST_INCOMPLETE sentinel below when the real body is in place.

import pytest

AC_UID = "{ac_uid}"
AC_TYPE = "{ac_type}"

# HOOK_CHECK is the AC's `check` object copied VERBATIM from the BA acceptance
# criteria. QA traces each test back to this; Dev MUST NOT hand-edit it.
HOOK_CHECK = {hook_check_repr}


def {func_name}():
    r"""
    {doc_given}
    {doc_when}
    {doc_then}
    """
    # TODO(dev): replace the line below with the real test body. While the
    # TEST_INCOMPLETE sentinel is present the test will hard-fail, marking
    # the AC as unimplemented for QA Phase 5.
    pytest.fail(f"TEST_INCOMPLETE: {{AC_UID}} — {summary}")
'''


def hook_check_repr(check):
    # Pretty multi-line python-literal repr so the dict reads cleanly in the file.
    return json.dumps(check, indent=4, ensure_ascii=False)


def main():
    with open(AC_PATH, "r", encoding="utf-8") as fh:
        ac_doc = json.load(fh)

    items = ac_doc.get("acceptance_criteria", [])
    os.makedirs(TASK_DIR, exist_ok=True)

    active_tests = []
    tests_created = []
    schema_warnings = []

    seen_uids = {}
    for item in items:
        ac_id = item["id"]
        ac_uid = item["ac_uid"]
        ac_type = item.get("type", "hook")
        check = item.get("check", {})

        if ac_uid in seen_uids:
            sys.stderr.write(
                "FATAL: duplicate ac_uid %s (%s and %s)\n"
                % (ac_uid, seen_uids[ac_uid], ac_id)
            )
            sys.exit(3)
        seen_uids[ac_uid] = ac_id

        if ac_type not in CANONICAL_TYPES:
            sys.stderr.write(
                "WARNING: %s has non-canonical type %r; treating as 'hook'\n"
                % (ac_id, ac_type)
            )
            ac_type = "hook"

        # Schema check: BA harness uses {method, probe, expect_exit} shell probes.
        status = "active"
        is_shell_probe = (
            isinstance(check, dict) and check.get("method") == "shell" and "probe" in check
        )
        canonical_ok = isinstance(check, dict) and REQUIRED_KEYS.get(ac_type, set()).issubset(
            set(check.keys())
        )
        if not (is_shell_probe or canonical_ok):
            status = "deferred_invalid_schema"
            msg = (
                "%s (%s/%s): check object missing required keys and is not a "
                "well-formed shell probe; marking deferred_invalid_schema"
                % (ac_id, ac_type, ac_uid)
            )
            sys.stderr.write("WARNING: %s\n" % msg)
            schema_warnings.append(msg)
        elif is_shell_probe and not canonical_ok:
            # Valid for this harness, but note the variance from the canonical
            # per-type schema (does not block dev).
            schema_warnings.append(
                "%s (%s): check uses harness shell-probe shape {method,probe,"
                "expect_exit} (non-canonical for type %s but valid) — status active"
                % (ac_id, ac_uid, ac_type)
            )

        func_name = "test_%s" % normalize_id(ac_id)
        file_basename = "test_%s_%s.py" % (normalize_id(ac_id), ac_uid)
        file_rel = "tests/generated/%s/%s" % (TASK_ID, file_basename)
        file_abs = os.path.join(TASK_DIR, file_basename)

        body = SKELETON_TMPL.format(
            task_id=TASK_ID,
            ac_id=ac_id,
            ac_uid=ac_uid,
            ac_type=ac_type,
            func_name=func_name,
            hook_check_repr=hook_check_repr(check),
            doc_given=docstring_block(item.get("given", ""), "GIVEN:"),
            doc_when=docstring_block(item.get("when", ""), "WHEN: "),
            doc_then=docstring_block(item.get("then", ""), "THEN: "),
            summary=one_line_summary(
                item.get("given", ""), item.get("when", ""), item.get("then", "")
            ),
        )
        with open(file_abs, "w", encoding="utf-8") as out:
            out.write(body)
        tests_created.append(file_rel)

        active_tests.append(
            {
                "task_id": TASK_ID,
                "ac_id": ac_id,
                "ac_uid": ac_uid,
                "type": ac_type,
                "file": file_rel,
                "status": status,
                "hook_check": check,
            }
        )

    # --- per-task manifest ---
    per_task = {
        "schema_version": "1.1",
        "task_id": TASK_ID,
        "generated_at": now_iso(),
        "active_tests": active_tests,
        "archived_tests": [],
    }
    with open(PER_TASK_MANIFEST, "w", encoding="utf-8") as out:
        json.dump(per_task, out, indent=2, ensure_ascii=False)
        out.write("\n")

    # --- global index upsert ---
    if os.path.exists(GLOBAL_INDEX):
        with open(GLOBAL_INDEX, "r", encoding="utf-8") as fh:
            index = json.load(fh)
    else:
        index = {"schema_version": "1.0", "kind": "index", "tasks": []}
    if "tasks" not in index or not isinstance(index.get("tasks"), list):
        index["tasks"] = []
    index.setdefault("schema_version", "1.0")
    index["kind"] = "index"
    entry = {
        "task_id": TASK_ID,
        "manifest_path": "tests/generated/%s/manifest.json" % TASK_ID,
    }
    replaced = False
    for i, t in enumerate(index["tasks"]):
        if t.get("task_id") == TASK_ID:
            index["tasks"][i] = entry
            replaced = True
            break
    if not replaced:
        index["tasks"].append(entry)
    with open(GLOBAL_INDEX, "w", encoding="utf-8") as out:
        json.dump(index, out, indent=2, ensure_ascii=False)
        out.write("\n")

    # --- report ---
    report = {
        "task_id": TASK_ID,
        "timestamp": now_iso(),
        "manifest_path": "tests/generated/%s/manifest.json" % TASK_ID,
        "global_index_path": "tests/generated/manifest.json",
        "global_index_kind": "index",
        "trigger_gate": {
            "complexity_tier": "COMPLEX",
            "risk_level": "high",
            "fired": True,
        },
        "tests_created": tests_created,
        "tests_archived": [],
        "tests_skipped_idempotent": [],
        "schema_notes": schema_warnings,
        "active_count": len(active_tests),
        "deferred_invalid_schema_count": sum(
            1 for t in active_tests if t["status"] == "deferred_invalid_schema"
        ),
        "status": "completed",
    }
    with open(REPORT_PATH, "w", encoding="utf-8") as out:
        json.dump(report, out, indent=2, ensure_ascii=False)
        out.write("\n")

    print("CREATED %d skeleton files" % len(tests_created))
    print("active_tests=%d deferred_invalid_schema=%d"
          % (len(active_tests), report["deferred_invalid_schema_count"]))
    print("per_task_manifest=%s" % PER_TASK_MANIFEST)
    print("global_index=%s (tasks now %d)" % (GLOBAL_INDEX, len(index["tasks"])))
    print("report=%s" % REPORT_PATH)


if __name__ == "__main__":
    main()
